    !isCampaignsRoute &&
    !isKanbanRoute &&
    !isContactsRoute &&
    !isTagsRoute &&
    !isFunnelRoute &&
    !isIntegrationsRoute &&
    !isSchedulingRoute &&
    !isReservationsRoute &&
    !isLeadQualificationRoute;
  const isToolsRoute =
    isPlansRoute ||
    isSettingsRoute ||
    isSubscribeRoute ||
    isMassSendRoute ||
    isCampaignsRoute ||
    isKanbanRoute ||
    isContactsRoute ||
    isTagsRoute ||
    isFunnelRoute ||
    isIntegrationsRoute ||
    isSchedulingRoute ||
    isReservationsRoute ||
    isLeadQualificationRoute;
  const [toolsOpen, setToolsOpen] = useState(true);
  const [toolsPickerOpen, setToolsPickerOpen] = useState(false);

  const goToSection = (view: "conversations" | "connection" | "stats" | "agent") => {
    setSelectedView(view);
    if (location !== "/" && !location.startsWith("/dashboard")) {
      setLocation("/dashboard");
    }
  };

  useEffect(() => {
    if (isToolsRoute) {
      setToolsOpen(true);
    }
  }, [isToolsRoute]);

  const handleLogout = async () => {
    try {
      // Limpa a sessÃ£o local do Supabase (token)
      await supabase.auth.signOut();
    } catch (err) {
      console.error("Erro ao sair (supabase):", err);
    }

    try {
      // Limpa a sessÃ£o de servidor (se existir)
      await fetch("/api/logout", { credentials: "include" });
    } catch (err) {
      console.warn("Falha ao chamar /api/logout:", err);
    }

    try {
      // Limpa cache de consultas relacionadas a auth
      await queryClient.invalidateQueries({ queryKey: ["/api/auth/user"] });
      await queryClient.clear();
    } catch {}

    // Redireciona para tela de login
    setLocation("/login");
  };

  useEffect(() => {
    if (!isLoading && !isAuthenticated) {
      toast({
        title: "NÃ£o autorizado",
        description: "VocÃª precisa fazer login. Redirecionando...",
        variant: "destructive",
      });
      setTimeout(() => {
        window.location.href = "/api/login";
      }, 500);
      return;
    }
  }, [isAuthenticated, isLoading, toast]);

  const { data: connection } = useQuery<WhatsappConnection>({
    queryKey: ["/api/whatsapp/connection"],
    enabled: isAuthenticated,
  });

  const { data: agentConfig } = useQuery<AiAgentConfig | null>({
    queryKey: ["/api/agent/config"],
  });

